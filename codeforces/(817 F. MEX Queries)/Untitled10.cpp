  for (auto&& [t, L, R]: query) {
    --L;
    X.eb(L);
    X.eb(R);
  }
  X.eb(0);
  UNIQUE(X);
  for (auto&& [t, L, R]: query) { L = LB(X, L), R = LB(X, R); }

  Lazy_SegTree<ActedMonoid_Sum_Affine<int>> seg(vc<int>(len(X), 1));

  for (auto&& [t, L, R]: query) {
    if (t == 1) { seg.apply(L, R, {0, 0}); }
    if (t == 2) { seg.apply(L, R, {0, 1}); }
    if (t == 3) { seg.apply(L, R, {-1, 1}); }
    auto check = [&](auto e) -> bool { return e == 0; };
    int k = seg.max_right(check, 0);
    print(1 + X[k]);
  }
